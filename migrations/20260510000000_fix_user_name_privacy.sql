-- 用户隐私保护：将 af_user 表中以手机号或邮箱作为名称的用户，更新为随机昵称
-- 格式：小马用户 + 6位随机数字（补零对齐）
-- 覆盖情况：手机号（8~15位纯数字，可带+前缀）、邮箱地址、空名称

UPDATE af_user
SET name = '小马用户' || LPAD(FLOOR(RANDOM() * 1000000)::INTEGER::TEXT, 6, '0')
WHERE
  -- 手机号：纯数字8~15位，可带+前缀（如 18810755129 或 +8618810755129）
  name ~ '^\+?[0-9]{8,15}$'
  -- 邮箱地址
  OR name ~ '^[A-Za-z0-9._%+\-]+@[A-Za-z0-9.\-]+\.[A-Za-z]{2,}$'
  -- 空名称兜底
  OR name = '';
